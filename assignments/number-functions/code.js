function add(x, y){
  return x+y
}
function subtract(x, y){
  return x - y
}
function multiply(x, y){
  return x * y
}
function divide(x, y){
  return x / y
}
function mod(x, y){
  return x % y
}
function averageOfTwo(x, y){
  return (x + y)/2
}
function averageOfThree(x, y, z){
  return (x + y + z)/3
}
function distance(x, y){
  return Math.abs(x-y)
}
function manhattanDistance(x1, y1, x2, y2){
  return Math.abs(x1 - x2) + Math.abs(y1 - y2)
}